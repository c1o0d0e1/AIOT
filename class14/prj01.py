#########################匯入模組#########################
import mcu
import time
import urandom as random

#########################函式與類別定義#########################

#########################宣告與設定#########################
mp3 = mcu.MP3()

#########################主程式#########################
# 自動從10進位轉換為16進位
print(f"10進位的100轉為16進位是{hex(100)}")
mp3.start(
    volume=int(hex(100), 16), song=int(hex(random.randint(1, 71)), 16)
)  # 播放第1首歌，音量100
time.sleep(5)  # 播放5秒鐘
mp3.stop()  # 停止播放
