#########################宣告與設定#########################
wi = mcu.wifi()  # 建立 WiFi 物件
wi.setup(ap_active=False, sta_active=True)  # 設定 WiFi 模組工作模式
# 選擇要連接的wifi(搜尋 wifi)
wi.scan()
# 連接到指定的 wifi 熱點
if wi.connect("Singular_AI", "Singular#1234"):
    print(f"IP={wi.ip}")  # 顯示連接成功的 IP 位址
